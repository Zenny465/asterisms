/**
 * @file constellations_data.js
 * @brief ユーザーが作成した星座のデータを保持するためのファイルです。
 *        初期状態では空のデータまたはサンプルデータを定義します。
 *        stars_func.js がこのデータを読み込み、ローカルストレージに保存・読み込みを行います。
 */

// ここにユーザーが作成・保存した星座の初期データを定義できます。
// ただし、通常はローカルストレージから動的に読み込まれるため、
// ほとんどの場合、初期値は空の配列で問題ありません。
//
// 例:
// const MY_SAVED_CONSTELLATIONS = [
//   {
//     id: "sample_con_1",
//     name: "Sample Constellation One",
//     starIndices: [0, 10, 20, 30], // STARS_DATA_WITH_INDEX からのインデックス
//     color: "#FFD700" // 星座の色 (HEX)
//   },
//   {
//     id: "sample_con_2",
//     name: "Bright Diamond",
//     starIndices: [5, 15, 25, 35, 45],
//     color: "#00BFFF"
//   }
// ];

// ローカルストレージからの読み込みを優先するため、初期値は空の配列とします。
// stars_func.js がこの変数にアクセスしてデータを管理します。
window.MY_SAVED_CONSTELLATIONS = [];

// もし、初期状態でいくつかのサンプル星座を表示したい場合は、上記例のように記述してください。
// しかし、`stars_func.js`がローカルストレージからデータを読み込むため、
// このファイルに記述されたデータは、ローカルストレージに何も保存されていない初回起動時にのみ
// 参照されるか、あるいは完全に無視される可能性があります。
// 現在の実装では、`stars_func.js`で`localStorage.getItem('myConstellations')`を直接読み込んでいるため、
// このファイルの内容は初期データとしては使われず、実質的に空のファイルとなります。

// 将来的にサーバーサイドと連携する場合などは、このファイルが初期データ供給源になるかもしれません。